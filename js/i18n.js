// i18n.js
// Soporte multiidioma (español / inglés).
// - t(key): traduce cadenas de interfaz.
// - tr(obj, field): traduce un campo de un objeto de contenido (usa field_<lang> si existe).

export const LANGS = [
  { id: 'es', label: 'Español', flag: '🇪🇸' },
  { id: 'ca', label: 'Català', flag: '🌐' },
  { id: 'en', label: 'English', flag: '🇬🇧' },
];

const SUPPORTED = ['es', 'en', 'ca'];
let LANG = 'es';

export function setLang(l) {
  LANG = SUPPORTED.includes(l) ? l : 'es';
}
export function getLang() {
  return LANG;
}

/** Traduce un campo de contenido: devuelve obj[field_<lang>] si existe, si no obj[field]. */
export function tr(obj, field) {
  if (!obj) return '';
  if (LANG !== 'es') {
    const v = obj[field + '_' + LANG];
    if (v != null && v !== '') return v;
  }
  return obj[field] ?? '';
}

/** Elige entre arrays según el idioma (es / en / ca). */
export function pickArr(esArr, enArr, caArr) {
  if (LANG === 'en' && enArr) return enArr;
  if (LANG === 'ca' && caArr) return caArr;
  return esArr;
}

/** Traduce una cadena de interfaz por su clave. */
export function t(key, params) {
  const dict = UI[LANG] || UI.es;
  let s = (dict[key] != null) ? dict[key] : (UI.es[key] != null ? UI.es[key] : key);
  if (params) {
    for (const k of Object.keys(params)) s = s.replace(new RegExp('\\{' + k + '\\}', 'g'), params[k]);
  }
  return s;
}

